    private void txtMasaEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtMasaEkleActionPerformed
        ArrayList<Masa> masalar = (ArrayList<Masa>) new MasaCrud().read("");
        boolean isThereMasa = false;
        for (Masa masa : masalar) {
            if (masa.getMasaAdi().equalsIgnoreCase(txtMasaAdi.getText().trim())) {
                JOptionPane.showMessageDialog(this, "Eklemek istediğiniz masa sistemde mevcut, Lütfen geçerli bir masa ismi giriniz!!!");
                isThereMasa = true;
            }
        }
        if (!isThereMasa) {
            Masa masa = new Masa();
            masa.setMasaAdi(txtMasaAdi.getText().trim());
            masa.setDurum(1);
            masa.setKapasite(0);

            if (new MasaCrud().create(masa)) {
                JOptionPane.showMessageDialog(this, "Kayıt başarılı...");
                FrmPersonelAnaSayfa.refresh();
                dispose();
            }
        }
    }//GEN-LAST:event_txtMasaEkleActionPerformed

