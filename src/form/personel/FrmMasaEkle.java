    void kapasiteDoldur() {
        DefaultComboBoxModel dcbm = new DefaultComboBoxModel();
        dcbm.addElement("Kapasite");
        String query = "Select * from masakapasite";
        try {
            ResultSet rs = new DB().baglan().executeQuery(query);
            while (rs.next()) {
                
                dcbm.addElement(rs.getInt("masa_kapasite"));
            }
        } catch (SQLException e) {
            System.err.println("kapasiteDoldur: " +e);
        }
        cmbKapasite.setModel(dcbm);
    }

